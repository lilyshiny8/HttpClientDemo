package JavaSyntax;

public class part1_19_位运算符 {
    public static void main(String[] args) {
        //
        int numA = 19;
        int numB = 5;
        System.out.println(19&5);
        System.out.println(19|5);
        System.out.println(2 << 2);

        int numC = 023;//八进制
        System.out.println(numC);

        int numD = 0x23;//16进制
        System.out.println(numD);
    }
}
